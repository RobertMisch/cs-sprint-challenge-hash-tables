class Ticket:
    def __init__(self, source, destination):
        self.source = source
        self.destination = destination


def reconstruct_trip(tickets, length):
    """
    YOUR CODE HERE
    """
    route=[]
    ticket_index={}
    #build ticket index
    for ticket in tickets:
        ticket_index[ticket.source] = ticket.destination
    #get trip starter
    route.append(ticket_index["NONE"])
    for i in range(length-1):
        current_ticket= route[-1]
        route.append(ticket_index[current_ticket])
    return route

